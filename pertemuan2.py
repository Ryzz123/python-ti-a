# ==========|---CONTOH VARIABEL---|==============
ayam = "Berjenis Unggas"
angka = 123456789

# mengakses variabel(menampilkan)
print(ayam)
print(angka)
print(ayam, +angka)

# Output :
# Berjenis Unggas
# 123456789
# Berjenis Unggas 123456789

# ==========|---CONTOH PERATURAN PENULISAN VARIABEL---|==============
